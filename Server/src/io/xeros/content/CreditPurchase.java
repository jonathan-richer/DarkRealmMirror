package io.xeros.content;

/**
 * Handles the credits which can be purchased
 * @author C.T for RuneRogue
 *
 */
public enum CreditPurchase {
	
	/**
	 * Allows for free teleports
	 */
	FREE_TELEPORTS,
	
	/**
	 * Immunity from farming disease
	 */
	DISEASE_IMUNITY,
	
	/**
	 * Hides wilderness kills from kill feed
	 */
	HIDE_WILDERNESS_KILLS,
	
}